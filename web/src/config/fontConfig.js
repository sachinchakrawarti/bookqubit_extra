// Helper function to get fonts for a specific language
export const getFontsForLanguage = (langCode) => {
  return languageFonts[langCode]?.fonts || languageFonts.en.fonts;
};

// Helper function to get a specific font
export const getFontById = (langCode, fontId) => {
  const fonts = getFontsForLanguage(langCode);
  return fonts.find(font => font.id === fontId) || fonts[0];
};

// Helper function to load Google Font
export const loadGoogleFont = (font) => {
  if (!font || !font.googleFont) return;
  
  const linkId = `google-font-${font.id}`;
  if (!document.getElementById(linkId)) {
    const link = document.createElement('link');
    link.id = linkId;
    link.href = font.googleFont;
    link.rel = 'stylesheet';
    document.head.appendChild(link);
  }
};

// Helper function to apply font to document
export const applyFont = (fontFamily) => {
  if (fontFamily) {
    document.documentElement.style.setProperty('--font-primary', fontFamily);
    document.body.style.fontFamily = fontFamily;
  }
};

// Default fonts for each language (first font in list)
export const defaultFonts = Object.keys(languageFonts).reduce((acc, lang) => {
  acc[lang] = languageFonts[lang].fonts[0].family;
  return acc;
}, {});

// Get default font for a language
export const getDefaultFontForLanguage = (langCode) => {
  const fonts = getFontsForLanguage(langCode);
  return fonts[0];
};