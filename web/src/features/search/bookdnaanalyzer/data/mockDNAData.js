// Get DNA data for a specific book
export const getBookDNA = (bookId) => {
  return mockDNAData[bookId] || null;
};

// Get all book DNA data
export const getAllDNAData = () => {
  return Object.values(mockDNAData);
};

// Compare two books' DNA
export const compareDNA = (bookId1, bookId2) => {
  const book1 = getBookDNA(bookId1);
  const book2 = getBookDNA(bookId2);

  if (!book1 || !book2) return null;

  const compareCategory = (category) => {
    const keys = Object.keys(book1.dna[category]);
    const differences = {};
    let totalDiff = 0;

    keys.forEach((key) => {
      const val1 = book1.dna[category][key];
      const val2 = book2.dna[category][key];
      differences[key] = Math.abs(val1 - val2);
      totalDiff += Math.abs(val1 - val2);
    });

    return {
      differences,
      averageDiff: totalDiff / keys.length,
    };
  };

  return {
    book1: { id: book1.id, title: book1.title, dnaScore: book1.dnaScore },
    book2: { id: book2.id, title: book2.title, dnaScore: book2.dnaScore },
    themes: compareCategory("themes"),
    writingStyle: compareCategory("writingStyle"),
    emotionalTone: compareCategory("emotionalTone"),
    complexity: compareCategory("complexity"),
    overallSimilarity: 100 - (compareCategory("themes").averageDiff +
      compareCategory("writingStyle").averageDiff +
      compareCategory("emotionalTone").averageDiff +
      compareCategory("complexity").averageDiff) / 4,
  };
};